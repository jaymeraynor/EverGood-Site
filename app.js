const state = {
  lang: new URLSearchParams(location.search).get("lang") || localStorage.getItem("eg_lang") || "zh-Hant",
  filter: "all",
  cart: JSON.parse(localStorage.getItem("eg_cart") || "{}")
};

const formatPrice = (value) => new Intl.NumberFormat("zh-TW", {
  style: "currency",
  currency: "TWD",
  maximumFractionDigits: 0
}).format(value);

function t(key) {
  return translations[state.lang]?.[key] || translations["zh-Hant"][key] || key;
}

function localizePage() {
  document.documentElement.lang = state.lang;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });
  document.title = state.lang === "en"
    ? "EverGood Beauty Shop | EXO Skincare and Prasara Herbal Care"
    : state.lang === "ja"
      ? "EverGood Beauty Shop | EXO スキンケアと Prasara ハーブケア"
      : "EverGood Beauty Shop | EXO 金萃系列與 Prasara 草本護理";
  document.querySelector("meta[name='description']").setAttribute("content", state.lang === "en"
    ? "Shop Acureal EXO skincare and Prasara Sri Lankan herbal cleansing and oral care in a multilingual ecommerce storefront."
    : state.lang === "ja"
      ? "Acureal EXO スキンケアと Prasara スリランカ発想のハーブケアを扱う多言語 EC サイト。"
      : "EverGood Beauty Shop 提供 Acureal EXO 金萃外泌體保養系列、Prasara 斯里蘭卡草本清潔皂、沐浴皂與多效牙膏。");
  document.getElementById("language").value = state.lang;
}

function renderProducts() {
  const grid = document.getElementById("productGrid");
  const visible = products.filter((product) => state.filter === "all" || product.category === state.filter);
  grid.innerHTML = visible.map((product) => `
    <article class="product-card" itemscope itemtype="https://schema.org/Product">
      <div class="product-media">
        <img src="${product.image}" alt="${product.name[state.lang]}" loading="lazy" itemprop="image">
      </div>
      <div class="product-body">
        <span class="product-brand" itemprop="brand">${product.brand}</span>
        <h3 itemprop="name">${product.name[state.lang]}</h3>
        <p itemprop="description">${product.description[state.lang]}</p>
        <div class="tag-list">${product.tags.map((tag) => `<span>${tag}</span>`).join("")}</div>
        <div class="product-purchase" itemprop="offers" itemscope itemtype="https://schema.org/Offer">
          <span class="price" itemprop="price" content="${product.price}">${formatPrice(product.price)}</span>
          <meta itemprop="priceCurrency" content="TWD">
          <link itemprop="availability" href="https://schema.org/InStock">
          <button type="button" data-add="${product.id}">${t("addToCart")}</button>
        </div>
      </div>
    </article>
  `).join("");
}

function saveCart() {
  localStorage.setItem("eg_cart", JSON.stringify(state.cart));
}

function renderCart() {
  const items = Object.entries(state.cart)
    .map(([id, qty]) => ({ ...products.find((product) => product.id === id), qty }))
    .filter((item) => item.id);
  const total = items.reduce((sum, item) => sum + item.price * item.qty, 0);
  const count = items.reduce((sum, item) => sum + item.qty, 0);

  document.getElementById("cartCount").textContent = count;
  document.getElementById("cartTotal").textContent = formatPrice(total);
  document.getElementById("cartItems").innerHTML = items.length
    ? items.map((item) => `
      <div class="cart-row">
        <img src="${item.image}" alt="${item.name[state.lang]}">
        <div>
          <h3>${item.name[state.lang]}</h3>
          <div class="qty">
            <button type="button" data-dec="${item.id}" aria-label="Decrease">−</button>
            <span>${item.qty}</span>
            <button type="button" data-inc="${item.id}" aria-label="Increase">+</button>
          </div>
        </div>
        <strong>${formatPrice(item.price * item.qty)}</strong>
      </div>
    `).join("")
    : `<p>${t("emptyCart")}</p>`;
}

function openCart() {
  document.getElementById("cartPanel").classList.add("is-open");
  document.getElementById("cartPanel").setAttribute("aria-hidden", "false");
  document.getElementById("overlay").classList.add("is-open");
}

function closeCart() {
  document.getElementById("cartPanel").classList.remove("is-open");
  document.getElementById("cartPanel").setAttribute("aria-hidden", "true");
  document.getElementById("overlay").classList.remove("is-open");
}

document.addEventListener("click", (event) => {
  const addId = event.target.dataset.add;
  const incId = event.target.dataset.inc;
  const decId = event.target.dataset.dec;

  if (addId) {
    state.cart[addId] = (state.cart[addId] || 0) + 1;
    saveCart();
    renderCart();
    openCart();
  }

  if (incId) {
    state.cart[incId] = (state.cart[incId] || 0) + 1;
    saveCart();
    renderCart();
  }

  if (decId) {
    state.cart[decId] = Math.max((state.cart[decId] || 0) - 1, 0);
    if (!state.cart[decId]) delete state.cart[decId];
    saveCart();
    renderCart();
  }
});

document.querySelectorAll(".filter").forEach((button) => {
  button.addEventListener("click", () => {
    state.filter = button.dataset.filter;
    document.querySelectorAll(".filter").forEach((item) => item.classList.toggle("is-active", item === button));
    renderProducts();
  });
});

document.getElementById("language").addEventListener("change", (event) => {
  state.lang = event.target.value;
  localStorage.setItem("eg_lang", state.lang);
  const url = new URL(location.href);
  url.searchParams.set("lang", state.lang);
  history.replaceState(null, "", url);
  localizePage();
  renderProducts();
  renderCart();
});

document.getElementById("cartToggle").addEventListener("click", openCart);
document.getElementById("cartClose").addEventListener("click", closeCart);
document.getElementById("overlay").addEventListener("click", closeCart);
document.getElementById("checkoutButton").addEventListener("click", () => alert(t("checkoutMessage")));

if (!translations[state.lang]) state.lang = "zh-Hant";
localizePage();
renderProducts();
renderCart();
